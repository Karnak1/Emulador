#include "memory.h"
#include <string.h>

void memory_init(Chip8 *chip8) {
    memset(chip8->memory, 0, MEMORY_SIZE);
    
    /* Carregar fonte CHIP-8 na memória (0x50-0x9F) */
    uint8_t fontset[80] = {
        0xF0, 0x90, 0x90, 0x90, 0xF0, /* 0 */
        0x20, 0x60, 0x20, 0x20, 0x70, /* 1 */
        0xF0, 0x10, 0xF0, 0x80, 0xF0, /* 2 */
        0xF0, 0x10, 0xF0, 0x10, 0xF0, /* 3 */
        0x90, 0x90, 0xF0, 0x10, 0x10, /* 4 */
        0xF0, 0x80, 0xF0, 0x10, 0xF0, /* 5 */
        0xF0, 0x80, 0xF0, 0x90, 0xF0, /* 6 */
        0xF0, 0x10, 0x20, 0x40, 0x40, /* 7 */
        0xF0, 0x90, 0xF0, 0x90, 0xF0, /* 8 */
        0xF0, 0x90, 0xF0, 0x10, 0xF0, /* 9 */
        0xF0, 0x90, 0xF0, 0x90, 0x90, /* A */
        0xE0, 0x90, 0xE0, 0x90, 0xE0, /* B */
        0xF0, 0x80, 0x80, 0x80, 0xF0, /* C */
        0xE0, 0x90, 0x90, 0x90, 0xE0, /* D */
        0xF0, 0x80, 0xF0, 0x80, 0xF0, /* E */
        0xF0, 0x80, 0xF0, 0x80, 0x80  /* F */
    };
    
    memcpy(&chip8->memory[0x50], fontset, 80);
}

void memory_write(Chip8 *chip8, uint16_t address, uint8_t value) {
    if (address < MEMORY_SIZE) {
        chip8->memory[address] = value;
    }
}

uint8_t memory_read(Chip8 *chip8, uint16_t address) {
    if (address < MEMORY_SIZE) {
        return chip8->memory[address];
    }
    return 0;
}

uint16_t memory_read_word(Chip8 *chip8, uint16_t address) {
    uint8_t high = memory_read(chip8, address);
    uint8_t low = memory_read(chip8, address + 1);
    return (high << 8) | low;
}
